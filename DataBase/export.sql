--  DDL for View VISTA_CIRUGIAS_C_P
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "CONSULTORIO"."VISTA_CIRUGIAS_C_P" ("CIRUGIAS", "CANTIDAD_PACIENTES") AS 
  SELECT hc.Cirugias, COUNT(cc.Condiciones_CC) AS Cantidad_Pacientes
FROM Historial_cirugias hc
JOIN Condiciones_cirugias cc ON hc.ID_cirugias = cc.Historial_CC
GROUP BY hc.Cirugias;
--------------------------------------------------------
--  DDL for View VISTA_DENTISTAS_C_P
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "CONSULTORIO"."VISTA_DENTISTAS_C_P" ("NOMBRE", "APELLIDO_PA", "CANTIDAD_PACIENTES") AS 
  SELECT d.Nombre, d.Apellido_PA, COUNT(c.ID_cita) AS Cantidad_Pacientes
FROM Dentista d
JOIN Cita c ON d.Numero_de_empleado = c.Dentista_Cita
GROUP BY d.Nombre, d.Apellido_PA;
--------------------------------------------------------
--  DDL for View VISTA_EXPEDIENTE_PACIENTE
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "CONSULTORIO"."VISTA_EXPEDIENTE_PACIENTE" ("ID_PACIENTE", "NOMBRE", "APELLIDO_PA", "APELLIDO_MA", "FECHA_NAC", "ID_CITA", "FECHA_REGISTRO", "ESTADO_CITA", "HORA", "DENTISTA_CITA", "DIAGNOSTICO", "OBSERVACIONES") AS 
  SELECT p.ID_Paciente, p.NOMBRE, p.APELLIDO_PA, p.APELLIDO_MA, p.FECHA_NAC,
       c.ID_CITA, c.FECHA_REGISTRO, c.ESTADO_CITA, c.HORA, c.DENTISTA_CITA,
       d.DIAGNOSTICO, d.OBSERVACIONES
FROM Paciente p
JOIN Cita c ON p.ID_Paciente = c.ID_Paciente
JOIN Diagnostico d ON c.ID_CITA = d.ID_CITA;
--------------------------------------------------------
--  DDL for View VISTA_GANANCIAS_TOTALES
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "CONSULTORIO"."VISTA_GANANCIAS_TOTALES" ("GANANCIAS_TOTALES") AS 
  SELECT SUM(tt.Costo * COUNT(tc.Tipo_de_trabajo_trabajo_cita)) AS Ganancias_Totales
FROM Cita c
JOIN Trabajo_cita tc ON c.ID_cita = tc.Cita_trabajo_cita
JOIN Tipo_de_trabajo tt ON tc.Tipo_de_trabajo_trabajo_cita = tt.ID_tipo_de_trabajo
GROUP BY tt.Costo;
--------------------------------------------------------
--  DDL for View VISTA_RECETA_COMPLETA_PACIENTE
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "CONSULTORIO"."VISTA_RECETA_COMPLETA_PACIENTE" ("MEDICAMENTO", "DOSIS", "FRECUENCIA", "FECHA_INICIO", "FECHA_TERMINO") AS 
  SELECT m.Medicamento, hm.Dosis, hm.Frecuencia, hm.Fecha_inicio, hm.Fecha_termino
FROM Historial_de_medicamento hm
JOIN Medicamento m ON hm.Medicamento_Historial = m.ID_medicamento
JOIN Diagnostico d ON hm.Diagnostico_Historial = d.ID_DIAGNOSTICO
JOIN Cita c ON d.ID_CITA = c.ID_cita;
--------------------------------------------------------
--  DDL for View VISTA_TIPO_TRABAJO_MAS_PEDIDO
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "CONSULTORIO"."VISTA_TIPO_TRABAJO_MAS_PEDIDO" ("NOMBRE_TRABAJO") AS 
  SELECT "NOMBRE_TRABAJO" FROM (
    SELECT tt.Nombre_Trabajo
    FROM Tipo_de_trabajo tt
    JOIN Trabajo_cita tc ON tt.ID_tipo_de_trabajo = tc.Tipo_de_trabajo_trabajo_cita
    GROUP BY tt.Nombre_Trabajo
    ORDER BY COUNT(tc.Tipo_de_trabajo_trabajo_cita) DESC
)
WHERE ROWNUM = 1;
